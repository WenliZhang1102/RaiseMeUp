    private void butSetFoodActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_butSetFoodActionPerformed
        boolean foundFood = false;

        for (Map.Entry<Job, Integer> job : pet.getOwnedjobs().entrySet()){
            if (job.getKey().getTitle().equals(comboJobs.getSelectedItem().toString())){
                //System.out.println("spinvalue: " + spinPieceFood.getValue().toString());
                
                foundFood = true;
                if (Integer.parseInt(spinTimeworked.getValue().toString()) == job.getKey().getLength()){
                    RaiseMeUp.removeJobOwned(pet, job.getKey());
                    pet.getOwnedjobs().remove(job.getKey());
                } else {
                    job.setValue(Integer.parseInt(spinTimeworked.getValue().toString()));
                    RaiseMeUp.updateJobOwned(pet, job.getKey(), job.getValue());
                }
                break;
            }
        }
        if (!foundFood){
            for (Map.Entry<Integer, Job> job : jobs.entrySet()){
                if (job.getValue().getTitle().equals(comboJobs.getSelectedItem().toString()) && 
                        Integer.parseInt(spinTimeworked.getValue().toString()) < job.getValue().getLength()){
                    pet.getOwnedjobs().put(job.getValue(), Integer.parseInt(spinTimeworked.getValue().toString()));
                    RaiseMeUp.newJobOwned(pet, job.getValue(), Integer.parseInt(spinTimeworked.getValue().toString()));
                    break;
                }
            }
        }
        listJobs();

        System.out.println(comboJobs.getSelectedItem().toString());
    }//GEN-LAST:event_butSetFoodActionPerformed

