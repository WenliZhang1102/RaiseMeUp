    private void listJobs(){
        DefaultTableModel modelJob = (DefaultTableModel) tableJobs.getModel();
        modelJob.setRowCount(0);

        for (Map.Entry<Job, Integer> job : pet.getOwnedjobs().entrySet()){
            //System.out.println(i.getValue().getUsername());
            modelJob.addRow(new Object[]{job.getKey().getId(), job.getKey().getTitle(), job.getKey().getSpecies(),
            job.getKey().getLength(), job.getValue()});
            //System.out.println(((Food)item.getKey()).getName());
        }
    }

